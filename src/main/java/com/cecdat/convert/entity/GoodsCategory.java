package com.cecdat.convert.entity;

public class GoodsCategory {
    Integer id;
    String iconRrl;
    String isUse;
    String cnNme;
    String userId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getIconRrl() {
        return iconRrl;
    }

    public void setIconRrl(String iconRrl) {
        this.iconRrl = iconRrl;
    }

    public String getIsUse() {
        return isUse;
    }

    public void setIsUse(String isUse) {
        this.isUse = isUse;
    }

    public String getCnNme() {
        return cnNme;
    }

    public void setCnNme(String cnNme) {
        this.cnNme = cnNme;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
